#include <stdio.h>
int main(){
    int a = 7;
    /* OR
    int a;
    a = 7;
    */
    float b = 5.7;
    char c = 'D';

    printf("the value of a is : %d\n", a);
    printf("the value of b is : %f\n", b);
    printf("the value of c is : %c\n", c);
    /* HINT
    int type data_type - %d
    float type data_type - %f
    char type data_type - %c
    \n represent a new line .
    */

    return 0;
}
